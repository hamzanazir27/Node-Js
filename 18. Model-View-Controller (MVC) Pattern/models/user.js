const mongoose = require("mongoose");

// User Schema Definition
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  age: {
    type: Number,
    required: true,
  },
});

// Create and export User model
module.exports = mongoose.model("User", userSchema);
